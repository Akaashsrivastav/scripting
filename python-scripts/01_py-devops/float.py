# Float variables
num1 = 76
num2 = 23

# Basic Arithmetic
result1 = num1 + num2
print("The Addition of num1 & num2 is :", result1)

result2 = num1 - num2
print("The Subtraction of num1 & num2 is :", result2)

result3 = num1 * num2
print("The Multiplication of num1 & num2 is :", result3)

result4 = num1 / num2
print("The Division of num1 & num2 is :", result4)

# Rounding
result5 = round(3.14159265359, 7)  # Rounds to 2 decimal places
print("Rounded:", result5)